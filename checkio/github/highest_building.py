def highest_building(buildings):

    temp_build_size = 0
    building_size_list = []

    buildings_count = len(buildings[0])
    for i in range(buildings_count):
        for build in buildings:
            temp_build_size += build[i]
        building_size_list.append([i+1, temp_build_size])
        temp_build_size = 0

    building_size_list.sort(key=lambda x: x[1], reverse=True)

    return building_size_list[0]


